public class 習題4_7{
  public static void main(String[] 參數){
    int[] a=new int[5];
    for(int i=0;i<=3;++i)
      a[i]=2*i;
    System.out.println(a[0]+a[1]+a[2]+a[3]+a[4]);
    System.out.println(a[0]+"|"+a[1]+"|"+a[2]+"|"+a[3]+"|"+a[4]);
  } //方法:main() 定義區塊結束
} //類別: 習題4_7 定義區塊結束
